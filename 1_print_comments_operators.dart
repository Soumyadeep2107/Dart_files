void main() {
  print("Hello World");
  //this will help to print hello world in the console.
  print(3 + 2); // +,*,/,- all are the operators
  print('3' +
      '2'); //this will give the output 32 , as both strings are concatenated.
  print('4' * 10); //this will print 4 for 10 times.As 4 is considered as string
  print((5 / 4)); // will give the float value.
  // Note that DART follows BODMAS rule.
  // /*.....*/ is the syntax for multiline comment.
  // there are another type of comment is documentatio comment.
  print(75 / (5 + 2));
}
